# Creating an array
arr = [10, 20, 30, 40, 50]

# Accessing elements by index
print("Element at index 2:", arr[2])  # Output: 30

# Updating an element
arr[3] = 45

# Iterating over the array
print("Array elements:")
for elem in arr:
    print(elem)

# Array length
print("Array length:", len(arr))  # Output: 5
